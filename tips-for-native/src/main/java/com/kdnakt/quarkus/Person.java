package com.kdnakt.quarkus;

public class Person {
    private String first;
    private String last;

    public String getFirst() {
        return first;
    }

    public void setFirst(String first) {
        this.first = first;
    }

    public String getLast() {
        return last;
    }

    public void setValue(String last) {
        this.last = last;
    }
}